export function generateAlgorithms(trapScore: number): AlgorithmResult[] {
  return ALGO_NAMES.map((algo, i) => {
    const base = Math.max(5, Math.min(95, trapScore + (Math.random() - 0.5) * 30))
    const score = Math.round(base)
    const signal: AlgorithmResult['signal'] =
      score >= 70 ? 'TRAP' : score >= 50 ? 'WARN' : score >= 30 ? 'NEUTRAL' : 'SAFE'
    const details = ALGO_DETAILS[i]
    const detail = details[Math.floor(Math.random() * details.length)]
    return { id: i + 1, name: algo.name, engine: algo.engine, score, signal, detail, weight: algo.weight }
  })
}

export function generateOddsHistory(openHome: number, openAway: number, points = 12): OddsMovement[] {
  const openHdp = -0.5
  return Array.from({ length: points }, (_, i) => ({
    timestamp: new Date(Date.now() - (points - 1 - i) * 3600_000).toISOString(),
    home: parseFloat((openHome + (Math.random() - 0.5) * 0.08 * i * 0.3).toFixed(2)),
    draw: parseFloat((3.3 + (Math.random() - 0.5) * 0.1).toFixed(2)),
    away: parseFloat((openAway + (Math.random() - 0.5) * 0.08 * i * 0.3).toFixed(2)),
    hdp: parseFloat((openHdp + (Math.random() - 0.5) * 0.1 * i * 0.2).toFixed(2)),
    volume: Math.floor(80 + Math.random() * 220),
  }))
}

