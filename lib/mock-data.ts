function makeOddsHistory(openHome: number, openAway: number, openHdp: number) {
  return Array.from({ length: 12 }, (_, i) => ({
    timestamp: new Date(Date.now() - (11 - i) * 3600_000).toISOString(),
    home: parseFloat((openHome + (Math.random() - 0.5) * 0.08 * i * 0.3).toFixed(2)),
    draw: parseFloat((3.3 + (Math.random() - 0.5) * 0.1).toFixed(2)),
    away: parseFloat((openAway + (Math.random() - 0.5) * 0.08 * i * 0.3).toFixed(2)),
    hdp: parseFloat((openHdp + (Math.random() - 0.5) * 0.1 * i * 0.2).toFixed(2)),
    volume: Math.floor(80 + Math.random() * 220),
  }))
}

